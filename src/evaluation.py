def get_branch_corr(tree1, tree2):
    # Map bipartitions to their lengths
    bit_map1 = tree1.encode_bipartitions()
    map1 = {edge.bipartition: edge.length for edge in tree1.edges() if edge.length is not None}
    
    bit_map2 = tree2.encode_bipartitions()
    map2 = {edge.bipartition: edge.length for edge in tree2.edges() if edge.length is not None}
    
    # Align lengths based on shared bipartitions
    shared_bipartitions = set(map1.keys()) & set(map2.keys())
    
    lengths1 = [map1[bp] for bp in shared_bipartitions]
    lengths2 = [map2[bp] for bp in shared_bipartitions]
    
    # Calculate Pearson Correlation using Pandas
    return pd.Series(lengths1).corr(pd.Series(lengths2))

#better than correlation which ignores the tree if not exactly the same
def get_patristic_corr(tree1, tree2):
    # calculates correlation based on tip-to-tip distance
    #using distance matrix for both trees
    pdm1 = tree1.phylogenetic_distance_matrix()
    pdm2 = tree2.phylogenetic_distance_matrix()

    labels = [taxon.label for taxon in tree1.taxon_namespace]
    dists1 = []
    dists2 = []

    for i in range(len(labels)):
        for j in range(i + 1, len(labels)):
            t1, t2 = labels[i], labels[j]
            dists1.append(pdm1.distance(tree1.taxon_namespace.get_taxon(t1), 
                                        tree1.taxon_namespace.get_taxon(t2)))
            dists2.append(pdm2.distance(tree2.taxon_namespace.get_taxon(t1), 
                                        tree2.taxon_namespace.get_taxon(t2)))
            
    return pd.Series(dists1).corr(pd.Series(dists2))

def true_tree_analysis(tt_path, bt_paths,csv=0, output_dir=None):
    """
    True tree analysis takes in a true tree generated by ROSE software and compares 
    it to built trees to get Robinson Fould distance and correlation scores.

    Arguments:
        tt_path (str): The file path of the true tree being compared against

        bt_paths (str): A list of the file paths of the built trees being analyzed

        csv (int): Value to indicate csv export (1 or 0)

        output_dir (str): Path of desired output directory if CSV enabled

    Returns:
        name (str): The name of the tree (i.e JC_low_10_50).

        method (str): The type of tree generation method being compared.

        nRF_distance (float): Normalized Robinson-Fould measurement.

        b_corr (float): Branch Correlation score.
    """
    tns = dendropy.TaxonNamespace()
    true_tree = dendropy.Tree.get(path=tt_path, schema="newick", taxon_namespace=tns)

    results = []

    for path in bt_paths:
        # Extract metadata from filename (e.g., "UPGMA_JC_low.nwk")
        filename = os.path.basename(path)
        #grab method name ('ML', 'UPGMA', 'NJ', 'MP')
        method_name = filename.split('_')[-1][:-4] 
        tree_name = os.path.splitext(filename)[0]

        # Load built tree using the same namespace
        inf_tree = dendropy.Tree.get(path=path, schema="newick", taxon_namespace=tns)
        inf_tree.encode_bipartitions()
        
        # Robinson fould
        rf = treecompare.symmetric_difference(true_tree, inf_tree)
        
        # Max RF for bifurcating trees: sum of internal edges
        rf = treecompare.symmetric_difference(true_tree, inf_tree)
        
        # Updated logic to count valid internal bipartitions
        n_internal_true = 0
        for edge in true_tree.internal_edges():
            if edge.bipartition.leafset_bitmask is not None:
                n_internal_true += 1
                
        n_internal_inf = 0
        for edge in inf_tree.internal_edges():
            if edge.bipartition.leafset_bitmask is not None:
                n_internal_inf += 1
        
        max_rf = n_internal_true + n_internal_inf
        nrf = rf / max_rf if max_rf > 0 else 0

        # --- Branch Length Correlation ---
        p_corr = get_patristic_corr(true_tree, inf_tree)

        results.append({
            "name": tree_name,
            "method": method_name,
            "nRF_distance": round(float(nrf), 4),
            "p_corr": round(float(p_corr), 4)
        })

        df = pd.DataFrame(results)

        if csv == 1:
            if output_dir and not os.path.exists(output_dir):
                os.makedirs(output_dir)
            
            # Create a default name if none provided, or use the first tree's name
            out_name = f"analysis_results.csv"
            out_path = os.path.join(output_dir, out_name) if output_dir else out_name
            df.to_csv(out_path, index=False)
            #print(f"Results exported to {out_path}")

    return df

def tt_folder_analysis(
        true_trees_dir, built_trees_dir,
        csv_name, csv_out=1, output_dir="CSVs"
        ):
    """
    Wraps true_tree_analysis to process an entire directory of simulations.

    Arguments: 
        true_trees_dir: path of folder true trees are in
        built_trees_dir: path of folder built trees are in
        csv_name: desired csv name
        csv_out: Default is 1, can be 0 if no csv output desired
        output_dir: csv output folder, default is 'CSVs'

        returns pandas dataframe of results
    """
    # 1. Get all true tree files (assuming .tree or .nwk)
    # Adjust extension if ROSE outputs something specific
    true_tree_files = glob.glob(os.path.join(true_trees_dir, "*.tree")) + \
                      glob.glob(os.path.join(true_trees_dir, "*.nwk"))
    
    all_final_results = []

    for tt_path in true_tree_files:
        # Get the prefix (e.g., 'HKY_high_10_50')
        base_name = os.path.splitext(os.path.basename(tt_path))[0]
        print(f"Processing simulation: {base_name}...")

        # Find all built trees that start with or contain this prefix
        # This matches 'HKY_high_10_50_UPGMA.nwk', 'HKY_high_10_50_NJ.nwk', etc.
        search_pattern = os.path.join(built_trees_dir, f"{base_name}_*.nwk")
        associated_built_trees = glob.glob(search_pattern)

        if not associated_built_trees:
            print(f"  --> No built trees found for {base_name}. Skipping.")
            continue

        # call tree analysis method
        sim_df = true_tree_analysis(tt_path, associated_built_trees, csv=0)
        
        all_final_results.append(sim_df)

    # Combine everything into one master table
    if all_final_results:
        master_df = pd.concat(all_final_results, ignore_index=True)
        
        if csv_out:
            if not os.path.exists(output_dir): os.makedirs(output_dir)
            master_df.to_csv(os.path.join(output_dir, csv_name), mode = 'a', index=False)
            print(f"\nMaster results saved to {output_dir}")
            
        return master_df
    else:
        print("No matches found between folders.")
        return None

#PC MONITORING METRICS MEASURE AND SAVE TO CSV

def monitor_performance(function, *args, **kwargs):
    """
    Performance monitoring function wrapper
    Monitors the performance of a given function by tracking its execution time, CPU time, and peak RAM usage.
    Returns list of metrics and result of function
    """

    monitoring_results = {}
    process = psutil.Process(os.getpid())
    
    baseline_mem = process.memory_info().rss / (1024 * 1024)  # in MB

    # Shared variable to store the peak found by the thread
    peak_mem_during_op = [baseline_mem]
    stop_event = threading.Event()

    def track_memory():
        while not stop_event.is_set():
            # Current memory in MB
            current_mem = process.memory_info().rss / (1024 * 1024)
            if current_mem > peak_mem_during_op[0]:
                peak_mem_during_op[0] = current_mem
            # Small sleep to prevent high CPU usage from the monitor itself
            time.sleep(0.005) 

    # Start the memory monitoring thread
    mem_thread = threading.Thread(target=track_memory)
    mem_thread.start()

    # Performance monitoring start
    start_cpu_times = process.cpu_times().user + process.cpu_times().system
    start_time = time.perf_counter()

    try:
        result = function(*args, **kwargs)
    finally:
        # Performance monitoring end
        end_time = time.perf_counter()
        end_cpu_times = process.cpu_times().user + process.cpu_times().system
        
        # Stop the thread
        stop_event.set()
        mem_thread.join()

    #monitoring_results['execution_time_seconds'] = end_time - start_time
    monitoring_results['cpu_time_seconds'] = end_cpu_times - start_cpu_times
    monitoring_results['peak_ram_mb'] = peak_mem_during_op[0] - baseline_mem

    return result, monitoring_results

def save_performance_metrics(msa_file, metrics, tree, output_path='ROSE_performance.csv'):
    """
    Saves the computational performance metrics to a csv file. If the file has already been analyzed it will
    append to the end. Requires the msa_file and takes the monitoring results from the monitor_performance 
    function as a dictionary.
    """
    file_exists = os.path.isfile(output_path)
    with open(output_path, mode='a', newline='') as csvfile:
        fieldnames = ['msa_file','tree', 'seq_len', 'taxa_count', 'cpu_time_seconds', 'peak_ram_mb']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)

        if not file_exists:
            writer.writeheader()

        msa_file_trimmed = os.path.basename(msa_file)[:-len('.clustal')]

        row = {'msa_file': msa_file_trimmed}
        row['seq_len'] = get_msa_length(msa_file)
        row['tree'] = tree
        row['taxa_count'] = taxa_count(msa_file)
        row.update(metrics)
        writer.writerow(row)


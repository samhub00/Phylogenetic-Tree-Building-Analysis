def taxa_count(msa_file):
    """
    Counts taxa by counting lines in clustal file. 
    Good for empirical data
    """

    with open(msa_file, 'r') as f:
        # Skip the header line (CLUSTAL X...)
        next(f) 
        
        in_block = False
        count = 0
        
        for line in f:
            # strip() removes whitespace and newlines
            clean_line = line.strip()
            
            if clean_line:
                # We found the start of the first sequence block
                in_block = True
                count += 1
            elif in_block:
                # We hit the first empty line AFTER the first block
                # This is our answer.
                return count
                
        return count


